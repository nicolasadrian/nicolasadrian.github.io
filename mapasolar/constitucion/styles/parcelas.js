var style_parcelas = function(feature, resolution) {

    if (feature.hide === true) {
        return null;
    }


    var value = feature.get("kwh_m2dia");
    var style = categories_parcelas[value];
    var selectionStyle = categoriesSelected_parcelas[value];
    var labelText = "";
    var key = value + "_" + labelText

    if (!textStyleCache_parcelas[key]) {
        var text = new ol.style.Text({
            font: '16.5px Calibri,sans-serif',
            text: labelText,
            fill: new ol.style.Fill({
                color: "rgba(0, 0, 0, 255)"
            }),
        });
        textStyleCache_parcelas[key] = new ol.style.Style({
            "text": text
        });
    }
    var allStyles = [textStyleCache_parcelas[key]];
    var selected = lyr_parcelas.selectedFeatures;
    if (selected && selected.indexOf(feature) != -1) {
        allStyles.push.apply(allStyles, selectionStyle);
    } else {
        allStyles.push.apply(allStyles, style);
    }
    return allStyles;
};